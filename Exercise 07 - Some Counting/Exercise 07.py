#the format for for loops is range(start , stop , step). the stop value should be one value before the actual stopping value because it stops right before the actual stopping value.

#Write a loop that counts up from 0 to 50 in increments of 1.

for i in range(0 , 50 , 1):
    print(i)

#Write a loop that counts down from 50 to 0 in decrements of 1.

for i in range(50 , -1 , -1):
    print(i)    

